#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while (n--)
	{
		if (*s1 != *s2 || !*s1 || !*s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (0);
}
