#include "libft.h"

int		ft_atoi(const char *str)
{
	int n;
	int sign;

	sign = 1;
	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str != '\0' && ft_isdigit(*str))
		n = n * 10 + (*str++ - '0');
	return (n * sign);
}
