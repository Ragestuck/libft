#include "libft.h"

int		ft_isprime(int nb)
{
	int i;

	i = 2;
	if (nb <= 0 || nb == 1)
		return (0);
	while (1)
	{
		if ((nb % i) == 0)
		{
			if (i == nb)
				return (1);
			return (0);
		}
		i++;
	}
}
